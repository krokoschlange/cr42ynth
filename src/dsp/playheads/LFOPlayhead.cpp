#include "LFOPlayhead.h"

namespace cr42y
{

LFOPlayhead::LFOPlayhead(LFO* l) :
		lfo(l),
		lastPos(0)
{
}

LFOPlayhead::~LFOPlayhead()
{
}

float LFOPlayhead::getLastPos()
{
	return lastPos;
}

void LFOPlayhead::setLastPos(float newPos)
{
	if (newPos > 1)
	{
		newPos -= (int) newPos;
	}
	lastPos = newPos;
}

} /* namespace cr42y */
